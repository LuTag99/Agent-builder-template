from __future__ import annotations

from dataclasses import dataclass
from datetime import datetime, timezone
import json
from pathlib import Path
import re
import shutil


@dataclass(slots=True)
class ProjectSpec:
    project_name: str
    idea: str
    desired_output: str
    destination: Path
    stakeholders: str = ""
    constraints: str = ""
    tech_stack: str = ""
    no_gos: str = ""
    current_status: str = "kickoff_complete"
    priority: str = "high"
    quality_expectations: str = ""
    success_measures: str = ""
    notes: str = ""


def slugify(value: str) -> str:
    slug = re.sub(r"[^a-z0-9]+", "-", value.lower()).strip("-")
    return slug or "agent-project"


def generate_workspace(spec: ProjectSpec, *, force: bool = False) -> Path:
    destination = Path(spec.destination).expanduser().resolve()
    if destination.exists() and any(destination.iterdir()) and not force:
        raise FileExistsError(
            f"Destination '{destination}' already exists and is not empty. "
            "Use --force to write into an existing directory."
        )

    destination.mkdir(parents=True, exist_ok=True)

    template_root = Path(__file__).resolve().parent / "templates" / "workspace"
    shutil.copytree(template_root, destination, dirs_exist_ok=True)

    project_brief_template = destination / "PROJECT" / "zz - PROJECT_BRIEF.md"
    state_template = destination / "STATE" / "zz - run_state.json"

    project_brief_path = destination / "PROJECT" / "PROJECT_BRIEF.md"
    project_context_path = destination / "PROJECT" / "PROJECT_CONTEXT.md"
    architecture_path = destination / "PROJECT" / "ARCHITECTURE.md"
    delivery_plan_path = destination / "PROJECT" / "DELIVERY_PLAN.md"
    decisions_path = destination / "PROJECT" / "DECISIONS.md"
    task_path = destination / "TASKS" / "BACKLOG" / "001-project-kickoff-and-foundation.md"
    state_path = destination / "STATE" / "run_state.json"
    next_action_path = destination / "STATE" / "NEXT_ACTION.md"

    _replace_placeholder(project_brief_template, project_brief_path)
    _replace_placeholder(state_template, state_path)

    _write_text(destination / "README.md", _build_workspace_readme(spec))
    _write_text(project_brief_path, _build_project_brief(spec))
    _write_text(project_context_path, _build_project_context(spec))
    _write_text(architecture_path, _build_architecture(spec))
    _write_text(delivery_plan_path, _build_delivery_plan(spec))
    _write_text(decisions_path, _build_decisions(spec))
    _write_text(task_path, _build_initial_task(spec))
    _write_text(next_action_path, _build_next_action(spec))
    _write_state(state_path, spec)

    for gitkeep_path in (
        destination / "TASKS" / "ACTIVE" / ".gitkeep",
        destination / "TASKS" / "BLOCKED" / ".gitkeep",
        destination / "TASKS" / "DONE" / ".gitkeep",
        destination / "RUNS" / ".gitkeep",
        destination / "REPORTS" / ".gitkeep",
    ):
        gitkeep_path.parent.mkdir(parents=True, exist_ok=True)
        gitkeep_path.touch(exist_ok=True)

    return destination


def _write_text(path: Path, content: str) -> None:
    path.write_text(content.rstrip() + "\n", encoding="utf-8")


def _replace_placeholder(placeholder: Path, target: Path) -> None:
    if placeholder.exists():
        placeholder.replace(target)


def _build_workspace_readme(spec: ProjectSpec) -> str:
    generated_on = datetime.now(timezone.utc).strftime("%Y-%m-%d")
    return f"""# {spec.project_name}

This workspace was generated by `agent-builder-template` to support agent-operated delivery for this project.

## Project Summary

- Core idea: {spec.idea}
- Intended output: {spec.desired_output}
- Current status: {spec.current_status}
- Generated on: {generated_on}

## Start Here

- Review `PROJECT/PROJECT_BRIEF.md`
- Review `PROJECT/PROJECT_CONTEXT.md`
- Confirm `PROJECT/ARCHITECTURE.md` and `PROJECT/DELIVERY_PLAN.md`
- Start from `TASKS/BACKLOG/001-project-kickoff-and-foundation.md`
- Keep `STATE/run_state.json` current as the work progresses

## Delivery Flow

`Brief -> Clarify -> Plan -> Architect -> Build -> Critic -> Regression -> Report -> Next Action`

The user should mainly refine the brief and approve important decisions. The agents should carry the structured delivery work forward.
"""


