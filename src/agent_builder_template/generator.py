def _write_state(path: Path, spec: ProjectSpec) -> None:
    run_id = f"kickoff-{datetime.now(timezone.utc).strftime('%Y-%m-%d')}-001"
    risks = [
        "Requirements may evolve as the project context becomes more specific."
    ]
    if not spec.tech_stack:
        risks.append("The technical stack is still open and may affect implementation choices.")
    if spec.constraints:
        risks.append(f"Constraint to watch: {spec.constraints}")
    if spec.no_gos:
        risks.append(f"No-go area to respect: {spec.no_gos}")

    payload = {
        "project_name": spec.project_name,
        "last_run_id": run_id,
        "current_status": spec.current_status,
        "current_phase": "kickoff",
        "active_task": None,
        "backlog_count": 1,
        "blocked_count": 0,
        "completed_count": 0,
        "open_issues": [],
        "known_risks": risks,
        "approval_gates_pending": [
            "final architecture confirmation",
            "stack confirmation" if not spec.tech_stack else "stack validation against real delivery needs",
        ],
        "last_updated": datetime.now(timezone.utc).replace(microsecond=0).isoformat(),
    }
    path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")
